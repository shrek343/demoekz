<?php
// ============================================================
//  ВХОД (АВТОРИЗАЦИЯ) по паре логин-пароль
//  Неверный вход -> понятное уведомление.
//  Админ Admin26 / Demo20 входит здесь же (он есть в базе).
// ============================================================
 // Этапы — что надо написать (по порядку)

 // Подключить служебные файлы: соединение с БД и функции авторизации (старт сессии и т.п.).
 // Поймать отправку формы — проверить, что метод запроса POST.
 // Считать введённые данные — логин и пароль из $_POST.
 // Найти пользователя в базе по логину (через подготовленный запрос — защита от SQL-инъекций).
  //Сверить пароль через password_verify (сравнение с хэшем, а не с открытым текстом).
 // Запомнить пользователя в $_SESSION (id, ФИО, роль).
 // Перенаправить по роли: админа в admin.php, обычного — в cabinet.php.
 // Если не совпало — записать текст ошибки.
  //Вывести HTML: шапку, сообщения (успех/ошибка), форму входа, ссылку на регистрацию, подвал.


require_once __DIR__ . '/includes/db.php';   // подключаем $pdo — объект соединения с базой
require_once __DIR__ . '/includes/auth.php'; // подключаем функции авторизации (там же session_start())

$error = '';                                  // заранее заводим переменную под текст ошибки (пустая = ошибок нет)

// --- Обрабатываем форму ТОЛЬКО если она реально отправлена методом POST ---
if ($_SERVER['REQUEST_METHOD'] === 'POST') {

    $login    = trim($_POST['login'] ?? '');  // берём логин; trim — срезаем пробелы; ?? '' — если поля нет, ставим пустую строку
    $password = $_POST['password'] ?? '';     // берём пароль как есть (пробелы в пароле трогать нельзя)

    // Подготовленный запрос: ? — это «заглушка», куда безопасно подставится логин (защита от SQL-инъекций)
    $stmt = $pdo->prepare('SELECT * FROM users WHERE login = ?');
    $stmt->execute([$login]);                 // выполняем запрос, подставляя $login вместо ?
    $user = $stmt->fetch();                   // забираем найденную строку (или false, если такого логина нет)

    // Проверяем СРАЗУ два условия: пользователь найден И пароль совпал с хэшем в базе
    if ($user && password_verify($password, $user['password'])) {

        // --- запоминаем пользователя в сессии (теперь система «знает», кто вошёл) ---
        $_SESSION['user_id'] = $user['id'];   // кладём id — по нему потом отличаем пользователя
        $_SESSION['fio']     = $user['fio'];  // ФИО — чтобы выводить «Здравствуйте, ...»
        $_SESSION['role']    = $user['role']; // роль — admin или обычный пользователь

        // --- редирект по роли: если admin -> в админку, иначе -> в кабинет ---
        header('Location: ' . ($user['role'] === 'admin' ? 'admin.php' : 'cabinet.php'));
        exit;                                 // ОБЯЗАТЕЛЬНО останавливаем скрипт после header, иначе код пойдёт дальше
    }

    $error = 'Неверный логин или пароль';     // сюда дойдём, только если НЕ нашли юзера или пароль не подошёл
}

$active = 'login';                            // помечаем активный пункт меню (для подсветки в шапке)
require_once __DIR__ . '/includes/header.php'; // выводим общую шапку сайта (<head>, навигация и т.д.)
?>

<h1 class="h2 mb-3">Вход</h1>

<?php if (isset($_GET['registered'])): ?>
  <!-- если после регистрации редиректнули сюда с ?registered=1 — показываем зелёное сообщение -->
  <div class="alert alert-success">Регистрация успешна! Войдите в систему.</div>
<?php endif; ?>

<?php if ($error): ?>
  <!-- если в $error есть текст — показываем красное сообщение; e() экранирует HTML (защита от XSS) -->
  <div class="alert alert-danger"><?= e($error) ?></div>
<?php endif; ?>

<form method="POST">                          <!-- форма отправляется на этот же файл методом POST -->
  <div class="mb-2">
    <label class="form-label">Логин</label>
    <input type="text" name="login" class="form-control" required>   <!-- required — браузер не даст отправить пустым -->
  </div>
  <div class="mb-3">
    <label class="form-label">Пароль</label>
    <input type="password" name="password" class="form-control" required> <!-- type=password — символы скрыты звёздочками -->
  </div>
  <button class="btn btn-sea w-100">Войти</button>  <!-- кнопка отправки формы (w-100 — на всю ширину) -->
</form>

<!-- навигация на регистрацию (текст по заданию) -->
<p class="text-center mt-3"><a href="register.php">Еще не зарегистрированы? Регистрация</a></p>

